package com.cox.automotive.edi9.vo;

public class ProvinceMapping {

	private String customerType;

	private String ovcCountryCode;

	private String atProvince;

	private String ovcProvinceCode;

	private String ovcProvinceDescription;

	public String getCustomerType() {
		return customerType;
	}

	public void setCustomerType(String customerType) {
		this.customerType = customerType;
	}

	public String getOvcCountryCode() {
		return ovcCountryCode;
	}

	public void setOvcCountryCode(String ovcCountryCode) {
		this.ovcCountryCode = ovcCountryCode;
	}

	public String getAtProvince() {
		return atProvince;
	}

	public void setAtProvince(String atProvince) {
		this.atProvince = atProvince;
	}

	public String getOvcProvinceCode() {
		return ovcProvinceCode;
	}

	public void setOvcProvinceCode(String ovcProvinceCode) {
		this.ovcProvinceCode = ovcProvinceCode;
	}

	public String getOvcProvinceDescription() {
		return ovcProvinceDescription;
	}

	public void setOvcProvinceDescription(String ovcProvinceDescription) {
		this.ovcProvinceDescription = ovcProvinceDescription;
	}




}
